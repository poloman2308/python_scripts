def format_cells_percentage_sw_eol(work_sheet):
    # Apply percentage format to cells G2:I18
    for row in work_sheet.iter_rows(min_row=2, max_row=18, min_col=7, max_col=9):
        for cell in row:
            cell.number_format = '0.0%'

    # Rows to be formatted
    rows_to_format = [24, 26, 28, 31, 33, 35, 38, 40, 42, 45, 47, 49, 52, 54,
        56, 59, 61, 63, 66, 68, 70, 73, 75, 77, 80, 82, 84, 87, 89, 91, 94, 96,
        98, 101, 103, 105,
        108, 110, 112, 115, 17, 119, 122, 124, 126, 129, 131, 133, 136, 138,
        140, 143, 145, 147]

    # Apply percentage format to specific rows in columns F to Y
    for row in work_sheet.iter_rows(min_row=24, max_row=147, min_col=6, max_col=25):
        if row[0].row in rows_to_format:
            for cell in row:
                cell.number_format = '0.0%'


